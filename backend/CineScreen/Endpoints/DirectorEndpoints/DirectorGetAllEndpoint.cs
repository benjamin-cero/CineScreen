using CineScreen.Data;
using CineScreen.Helper;
using CineScreen.Helper.Api;
using Microsoft.AspNetCore.Mvc;
using Microsoft.EntityFrameworkCore;
using static CineScreen.Endpoints.DirectorEndpoints.DirectorGetAllEndpoint;

namespace CineScreen.Endpoints.DirectorEndpoints;

[Route("directors")]
public class DirectorGetAllEndpoint(ApplicationDbContext db) : MyEndpointBaseAsync
    .WithRequest<DirectorGetAllRequest>
    .WithResult<MyPagedList<DirectorGetAllResponse>>
{
    [HttpGet("filter")]
    public override async Task<MyPagedList<DirectorGetAllResponse>> HandleAsync([FromQuery] DirectorGetAllRequest request, CancellationToken cancellationToken = default)
    {
        var query = db.Directors
            .AsQueryable();

        // Filter by search query
        if (!string.IsNullOrWhiteSpace(request.Q))
        {
            query = query.Where(d => d.FirstName.Contains(request.Q) || 
                                   d.LastName.Contains(request.Q));
        }

        // Project to result type
        var projectedQuery = query.Select(d => new DirectorGetAllResponse
        {
            ID = d.ID,
            FirstName = d.FirstName,
            LastName = d.LastName
        });

        // Create paginated response with filter
        var result = await MyPagedList<DirectorGetAllResponse>.CreateAsync(projectedQuery, request, cancellationToken);

        return result;
    }

    public class DirectorGetAllRequest : MyPagedRequest
    {
        public string? Q { get; set; } = string.Empty;
    }

    public class DirectorGetAllResponse
    {
        public int ID { get; set; }
        public string FirstName { get; set; } = string.Empty;
        public string LastName { get; set; } = string.Empty;
    }
} 